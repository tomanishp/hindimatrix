function minimumSwaps(arr) {
    const n = arr.length;
    const arrCopy = [...arr];
    let swaps = 0;

    for (let i = 0; i < n - 1; i++) {
        // Find maximum element's index in unsorted portion
        let maxIdx = i;
        for (let j = i + 1; j < n; j++) {
            if (arrCopy[j] > arrCopy[maxIdx]) {
                maxIdx = j;
            }
        }

        // If maximum is not at current position, swap
        if (maxIdx !== i) {
            // Perform the swap
            [arrCopy[i], arrCopy[maxIdx]] = [arrCopy[maxIdx], arrCopy[i]];
            swaps++;
        }
    }

    return swaps - 1;
}

function minimumSwaps2(arrIn) {
    let swaps = 0;
    const n = arrIn.length;

    // Create array of [element, index] pairs for tracking positions
    const arrPos = arrIn.map((el, i) => [el, i]);

    //console.log('Starting min swap check');

    //console.log(arrPos);

    // Sort the array of pairs based on element values
    arrPos.sort((a, b) => a[0] - b[0]);

    //console.log(arrPos);

    // Visited array to keep track of processed elements
    const visited = new Array(n).fill(false);

    for (let i = 0; i < n; i++) {
        // If element is visited or already in correct position
        if (visited[i] || arrPos[i][1] === i) {
            continue;
        }

        // Find cycle size
        let cycleSize = 0;
        let j = i;
        while (!visited[j]) {
            visited[j] = true;
            j = arrPos[j][1];
            cycleSize++;
        }

        // Add minimum swaps needed for this cycle
        swaps += (cycleSize - 1);
    }

    return swaps;
}

