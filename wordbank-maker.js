function randomizeWordList(iSize = 3) {

    const items = iSize != 3 ? [...wordBank44] : [...wordBank33];
    const setCount = Math.floor(items.length / iSize);
    const arrayRef = [];
    const arrayFinal = [];
    let idx = 0;

    while (idx < setCount) {
        let selected = items.slice(idx * iSize, (idx + 1) * iSize);
        const orgSet = formatComplexWords(selected.join('').split(''));
        const shuffleSet = shuffleWordSet(iSize, [...orgSet]);

        const sOrgSet = '["' + orgSet.join('","') + '"]';
        const sShuffleSet = '["' + shuffleSet.join('","') + '"]';


        arrayRef.push(sOrgSet);
        arrayFinal.push(sShuffleSet);
        idx++;
    }
    console.log(arrayRef);
    console.log(arrayFinal);

    return arrayRef;
}


function shuffleWordSet(iSize = 3, aItems = []) {

    for (let i = aItems.length - 1; i > 0; i--) {
        let j = i;

        while (j == i) {
            j = Math.floor(Math.random() * (i + 1));
        }

        [aItems[i], aItems[j]] = [aItems[j], aItems[i]];
    }

    return aItems;
}


function formatComplexWords(allChars) {

    let outText = [];

    for (let i = 0; i < allChars.length; i++) {
        if (postMatra.includes(allChars[i])) {
            let t1 = outText.pop();
            let c1 = ([t1, allChars[i], allChars[i + 1]]).join('');
            outText.push(c1);
            i++;
        } else if (preMatra.includes(allChars[i])) {
            let t2 = outText.pop();
            let c2 = ([t2, allChars[i]]).join('');
            outText.push(c2);
        } else {
            outText.push(allChars[i]);
        }
    }
    return outText;
}